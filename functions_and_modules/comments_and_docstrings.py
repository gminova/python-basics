# one line comment
"""
Docstrings (documentation strings) serve
a similar purpose to comments, as they are 
designed to explain code. However, they are
more specific and have a different syntax. 
They are created by putting a multiline string 
containing an explanation of the function below 
the function's first line.
"""


def shout(word):
    """
    Print a word with an
    exclamation mark following it.
    """
    print(word + "!")


shout("spam")
